public class Implicado {

	private int id;
	private string nombre;
	private string rol;

	public int getId() {
		return this.id;
	}

	/**
	 * 
	 * @param id
	 */
	public void setId(int id) {
		this.id = id;
	}

	public string getNombre() {
		return this.nombre;
	}

	/**
	 * 
	 * @param nombre
	 */
	public void setNombre(string nombre) {
		this.nombre = nombre;
	}

	public string getRol() {
		return this.rol;
	}

	/**
	 * 
	 * @param rol
	 */
	public void setRol(string rol) {
		this.rol = rol;
	}

}